#!/usr/bin/env python3
import typing
"""
    This module contains `add(float, float) -> float` with type annotation.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers.
        Args:
            a: The first number (float).
            b: The second number (float).
        Return:
            The sum of `a` and `b` (float).
    """
    return a + b
