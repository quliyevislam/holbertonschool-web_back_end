#!/usr/bin/env python3
import typing
"""This module contains add() with type annotation"""


def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers.

    :param a: The first number (float).
    :param b: The second number (float).
    :return: The sum of `a` and `b` (float).
    """
    return a + b
